// دالة الحصول على بيانات المنتج
function getProductDetails(productId) {
    return productsDetails[productId] || null;
}

// دالة الحصول على منتجات مشابهة
function getSimilarProducts(currentProductId, limit = 4) {
    const allIds = Object.keys(productsDetails).map(Number);
    const similar = allIds.filter(id => id !== currentProductId).slice(0, limit);
    return similar.map(id => ({
        ...productsDetails[id],
        id: id
    }));
}
